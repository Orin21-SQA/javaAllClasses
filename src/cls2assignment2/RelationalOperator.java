package cls2assignment2;

public class RelationalOperator {
    public static void main(String[]args){
        int x=200;
        int y=350;
        System.out.println(x>y);//F
        System.out.println(x<y);//T
        System.out.println(x>=y);//F
        System.out.println(x<=y);//T
        System.out.println(x==y);//F
        System.out.println(x!=y);//T


    }
}
