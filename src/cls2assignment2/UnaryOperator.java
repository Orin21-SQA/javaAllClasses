package cls2assignment2;

public class UnaryOperator {
    public static void main(String[]args){
        int x=60;
        System.out.println(x++);//60
        System.out.println(x);//61
        System.out.println(++x);//62
        System.out.println(x);//62
        System.out.println(x--);//62
        System.out.println(--x);//60
    }
}
