package class4;

public class WhileLoopDo {
    public static void main (String[]args){
        int i=1;
        while (i<=100){
            System.out.println(i);
            i++;
        }

        int j=50;
        do {
            System.out.println(j);
            j++;
        }
        while (j<=50);
    }
}
